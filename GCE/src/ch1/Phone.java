package ch1;

public class Phone {

	private double number;
	private String brand;
	
	public void setNumber(double num) {
		number = num;
	}
	public void setBrand(String str) {
		brand = str;
	}
	
	public double getNumber() {
		return number;
	}
	public String getBrand() {
		return brand;
	}
}
